# Operadores:
# Ruby suporta vários operadores, incluindo aritméticos 
# (+, -, *, /), de comparação (==, !=, >, <, >=, <=) e lógicos (&&, ||, !).

x = 5
y = 3
puts x + y # Output: 8
puts x * y # Output: 15
puts x == y # Output: false
puts x != y # Output: true
puts x > y # Output: true
puts x < y # Output: false
puts x >= y # Output: true
puts x <= y # Output: false
puts ''
puts (x > y) && (y > x)  # Output: false
puts x > y || y > x  # Output: true